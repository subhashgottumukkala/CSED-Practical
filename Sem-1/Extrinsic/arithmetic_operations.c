#include <stdio.h>
int main ()
{
int a = 8;
int b = 56;
printf("Addition is %d\n", a + b);
printf("Division is %d\n", a / b);
printf("Multiplication is %d\n", a * b);
printf("Remainder is %d\n", a % b);
printf("Increment value of a is %d\n", ++a);
printf("Decrement value of a is %d\n", --a);
return 0;
}
